# Arithmatic Operators
print("The sum of 3+4 is ",3+4)
print("The sum of 3-4 is ",3-4)
print("The sum of 3*4 is ",3*4)
print("The sum of 3/4 is ",3/4)

# Assignment Operators
a = 34
a += 6 #Increase value of a 
a -= 6 #Decrease value of a
a *= 3 #Multiplies value of a
a /= 2 #Divides value of a
print(a) #Value printed is after all operations above are applied 

# Comparision Operators
b = 4>7
b = 4<7
b = 4>=7
b = 4<=7
b = 4==7
b = 4!=7
print(b) #Prints true or false(boolean)

# Logical Operators
bool1=True
bool2=False
print("the value of bool1 and bool2 is ",(bool1 and bool2))
print("the value of bool1 or bool2 is ",(bool1 or bool2))
print("the value of not bool2 is ",(not bool2))

